function [T, T_norm, vars] = ML_temp_prediction(x, y, L, kx, ky, R, To, Th)
% -----------------------------------------------------------------
% ML Predict temperature at (x,y) using the ML REGRESSION equation.
% Physics assumptions:
% --------------------
%  * Steady-state only (no transient)
%  * Square plate geometry only
%  * Single circular centered hole
%  * Uniform boundary conditions (T = To on hole, T = Th on plate edges)
%  * No internal heat generation
% -----------------------------------------------------------------
xc  = x - L/2;   
yc  = y - L/2;
xi  = xc / sqrt(kx);   
eta = yc / sqrt(ky);
phi = atan2(eta, xi);

cphi = cos(phi);  
sphi = sin(phi);

rho_inner = R / sqrt(kx * cphi^2 + ky * sphi^2);
rho_outer = (L/2) / max(abs(cphi) * sqrt(kx), abs(sphi) * sqrt(ky));

p  = log((sqrt(xi^2 + eta^2)) / rho_inner);
q  = log(rho_outer / rho_inner);
u  = p ./ q;
la = log(sqrt(kx/ky));
RL = R / L;
c2 = cos(2 * phi);
c4 = cos(4 * phi);
c6 = cos(6 * phi);

% Store intermediates for display
vars.xi  = xi; 
vars.eta = eta; 
vars.phi = phi;
vars.p   = p; 
vars.q   = q;
vars.la  = la; 
vars.RL  = RL; 
vars.c2  = c2; 
vars.c4  = c4;
vars.c6  = c6;
vars.rho_inner = rho_inner; 
vars.rho_outer = rho_outer;

% ---- POLYNOMIAL EVALUATION ----
C = PIMLC_150(u, q, la, RL, c2, c4, c6);

T_norm = (1 - u) + u*(1 - u) * C;

T = To + (Th - To) * T_norm;
end



